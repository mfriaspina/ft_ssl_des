#include "ft_ssl.h"

int	leftrotate(int x, int c)
{
	return (((x) << (c)) | ((x) >> (32 - (c))));
}

int	rightrotate(int x, int c)
{
	return (((x) >> (c)) | ((x) << (32 - (c))));
}

int	xor(int a, int b)
{
	return (a ^ b);
}
